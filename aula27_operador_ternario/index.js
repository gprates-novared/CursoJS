// Operador ternário ? :
// (condicao) ? 'Valor verdadeiro' : 'Valor falso';

const pontuacao = 1001;


if (pontuacao >= 1000){
    console.log('User VIP');
} else {
    console.log('User Comum');
}


const resultado = pontuacao >= 1000 ? 'User VIP ternario' : 'User Comum ternario'; // ternário


console.log(resultado)

